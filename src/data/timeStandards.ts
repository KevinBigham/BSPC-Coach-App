// Helper: convert "M:SS.HH" to hundredths
function t(str: string): number {
  const parts = str.split(':');
  if (parts.length === 2) {
    const mins = parseInt(parts[0]);
    const secParts = parts[1].split('.');
    const secs = parseInt(secParts[0]);
    const hundredths = parseInt(secParts[1] || '0');
    return mins * 6000 + secs * 100 + hundredths;
  }
  const secParts = str.split('.');
  return parseInt(secParts[0]) * 100 + parseInt(secParts[1] || '0');
}

